package edu.iastate.se329.domain;

import java.util.Map;

/**
 * Nice to have...maybe
 * @author SaldinBajric
 *
 */
public class MultipleChoiceQuiz {
	private Long userId;
	private Long id;
	
	private Map<String, String> quiz; //<Question, Answer>
	
	public MultipleChoiceQuiz() {}

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Map<String, String> getQuiz() {
		return quiz;
	}

	public void setQuiz(Map<String, String> quiz) {
		this.quiz = quiz;
	}

}
